    private void loadItemTable() {
        String[] columns = {"PO ID", "Item ID", "Restock Quantity", "Supplier ID"};
        DefaultTableModel model = new DefaultTableModel(columns, 0);

        POManager poManager = new POManager();
        List<PurchaseOrder> poList = poManager.load(); // Load all POs

        for (PurchaseOrder po : poList) {
            if ("Approved".equalsIgnoreCase(po.getStatus()) && !po.isStockUpdate()) {
                Object[] row = {
                    po.getPoID(),
                    po.getItemID(),
                    po.getQuantity(),
                    po.getSupplierID()
                };
                model.addRow(row);
            }
        }

        tableItemList.setModel(model);
    }
    
        private void setupTableSelectionListener() {
            tableItemList.getSelectionModel().addListSelectionListener(event -> {
            if (!event.getValueIsAdjusting()) {
                int selectedRow = tableItemList.getSelectedRow();
                if (selectedRow != -1) {

                    Object poId = tableItemList.getValueAt(selectedRow, 0);
                    Object itemId = tableItemList.getValueAt(selectedRow, 1);
                    Object Quantity = tableItemList.getValueAt(selectedRow, 2);
                    Object supplierId = tableItemList.getValueAt(selectedRow, 3);

                    poID = poId != null ? poId.toString() : "";
                    String itemID = itemId != null ? itemId.toString() : "";
                    String Restock = Quantity != null ? Quantity.toString() : "";
                    String supplierID = supplierId != null ? supplierId.toString() : "";
                    
                    ItemManager itemManager = new ItemManager();
                    Item item = itemManager.findItemById(itemID);
                    
                    txtItemID.setText(itemID);
                    txtItemName.setText(item != null ? item.getItemName() : "Unknown");
                    txtRestockQty.setText(Restock);
                    
                    UpdateButton.setEnabled(true);
                    UpdateButton.setBackground(Color.GREEN);                 
                } else {
                    UpdateButton.setEnabled(false);
                    UpdateButton.setBackground(Color.GRAY);
                }
            }
        });
    }

