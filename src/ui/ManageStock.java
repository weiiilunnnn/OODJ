    private void UpdateButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_UpdateButtonActionPerformed
        String currentItemId = txtItemID.getText().trim();
        int currentSalesQty = Integer.parseInt(txtRestockQty.getText().trim());

        // Step 1: Update item quantity
        ItemManager itemManager = new ItemManager();
        System.out.println(currentItemId);
        Item item = itemManager.findItemById(currentItemId);
        System.out.println(item);

        if (item == null) {
            JOptionPane.showMessageDialog(null, "Item not found.");
            return;
        }

        int newQty = item.getItemQty() + currentSalesQty;
        itemManager.updateQuantity(currentItemId, newQty);

        // Step 2: Set UpdatedStatus = true in PO
        POManager poManager = new POManager();
        System.out.println("PO id: " + poID);
        boolean poFound = poManager.updateStatus(poID);

        if (poFound) {
            JOptionPane.showMessageDialog(null, "Updated successfully!");
        } else {
            JOptionPane.showMessageDialog(null, "PO not found.");
        }

        // Refresh table to remove updated rows
        loadItemTable();
    }//GEN-LAST:event_UpdateButtonActionPerformed

