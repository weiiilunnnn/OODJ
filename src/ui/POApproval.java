    private void tblRequisitionsMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblRequisitionsMouseClicked
        int row = tblRequisitions.getSelectedRow();
        if (row >= 0) {
            txtPOID.setText(tblRequisitions.getValueAt(row, 0).toString());
            txtItemID.setText(tblRequisitions.getValueAt(row, 2).toString());
            txtItemName.setText(tblRequisitions.getValueAt(row, 3).toString());
            txtRestockQty.setText(tblRequisitions.getValueAt(row, 4).toString());
            txtSupplierID.setText(tblRequisitions.getValueAt(row, 8).toString());
            txtSupplierName.setText(tblRequisitions.getValueAt(row, 9).toString());
            txtUnitPrice.setText(tblRequisitions.getValueAt(row, 10).toString());

            try {
                SimpleDateFormat format = new SimpleDateFormat("yyyy-MM-dd");
                Date date = format.parse(tblRequisitions.getValueAt(row, 6).toString());
                txtPurchaseDate.setDate(date);
            } catch (Exception e) {
                e.printStackTrace();
            }

        }
    }//GEN-LAST:event_tblRequisitionsMouseClicked

    private void btnEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditActionPerformed
        int row = tblRequisitions.getSelectedRow();
        if (row == -1) {
            JOptionPane.showMessageDialog(this, "Please select a PO to edit.");
            return;
        }

        String poID = tblRequisitions.getValueAt(row, 0).toString();
        String prID = tblRequisitions.getValueAt(row, 1).toString();
        String itemID = tblRequisitions.getValueAt(row, 2).toString();
        String itemName = tblRequisitions.getValueAt(row, 3).toString();
        String quantity = tblRequisitions.getValueAt(row, 4).toString();
        String purchaseDate = tblRequisitions.getValueAt(row, 6).toString();
        String supplierID = tblRequisitions.getValueAt(row, 8).toString();
        String supplierName = tblRequisitions.getValueAt(row, 9).toString();
        String distance = tblRequisitions.getValueAt(row, 10).toString();
        String price = tblRequisitions.getValueAt(row, 10).toString();

        GeneratePO generatePO = new GeneratePO(
            user, poID, prID, itemID, itemName, quantity,
            purchaseDate, true, supplierID, supplierName, distance, price
        );
        generatePO.setVisible(true);
        this.dispose();
    }//GEN-LAST:event_btnEditActionPerformed

