    private void btnApproveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnApproveActionPerformed
        int row = tblRequisitions.getSelectedRow();
        if (row == -1) {
            JOptionPane.showMessageDialog(this, "Please select a PO to approve.");
            return;
        }

        String poID = tblRequisitions.getValueAt(row, 0).toString();

        POManager manager = new POManager();
        boolean success = manager.updatePOStatus(poID, "Approved");

        if (success) {
            JOptionPane.showMessageDialog(this, "PO approved successfully.");
            loadPOTable();
            clearDetails();
        } else {
            JOptionPane.showMessageDialog(this, "Approval failed.");
        }
    }//GEN-LAST:event_btnApproveActionPerformed

    private void btnRejectActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRejectActionPerformed
        int row = tblRequisitions.getSelectedRow();
        if (row == -1) {
            JOptionPane.showMessageDialog(this, "Please select a PO to reject.");
            return;
        }

        String poID = tblRequisitions.getValueAt(row, 0).toString();

        POManager manager = new POManager();
        boolean success = manager.updatePOStatus(poID, "Rejected");

        if (success) {
            JOptionPane.showMessageDialog(this, "PO rejected.");
            loadPOTable();
            clearDetails();
        } else {
            JOptionPane.showMessageDialog(this, "Rejection failed.");
        }
    }//GEN-LAST:event_btnRejectActionPerformed

