    private void loadPOTable() {
        DefaultTableModel model = new DefaultTableModel(new String[] {
            "PO ID", "PR ID", "Item ID", "Item Name", "Quantity", "PM ID", 
            "Date", "Status", "Supplier ID", "Supplier Name", "Unit Price"
        }, 0);

        List<String> lines = FileOperation.readRawLines("PurchaseOrder.txt");
        Map<String, String> itemMap = FileOperation.getItemMap();
        Map<String, String> supplierMap = FileOperation.getSupplierMap(); // <-- new

        for (String line : lines) {
            String[] parts = line.split(",");
            if (parts.length >= 11 && parts[6].equalsIgnoreCase("Approved") &&  parts[10].equalsIgnoreCase("false")) {
                String itemName = itemMap.getOrDefault(parts[2], "Unknown Item");
                String supplierName = supplierMap.getOrDefault(parts[7], "Unknown Supplier");
                

                model.addRow(new Object[] {
                    parts[0], parts[1], parts[2], itemName, parts[3],
                    parts[4], parts[5], parts[6], parts[7], supplierName, parts[8]
                });
            }
        }

        tblRequisitions.setModel(model);
    }
    
