    private void btnVerifyActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnVerifyActionPerformed
        int row = tblRequisitions.getSelectedRow();
        if (row == -1) {
            JOptionPane.showMessageDialog(this, "Please select a PO to verify.");
            return;
        }

        String poID = tblRequisitions.getValueAt(row, 0).toString();
        List<String> lines = FileOperation.readRawLines("PurchaseOrder.txt");
        boolean found = false;

        for (int i = 0; i < lines.size(); i++) {
            String[] parts = lines.get(i).split(",");
            if (parts.length >= 11 && parts[0].equals(poID)) {
                parts[10] = "true"; // set PO as verified
                lines.set(i, String.join(",", parts));
                found = true;
                break;
            }
        }

        if (found) {
            FileOperation.writeRawLines("PurchaseOrder.txt", lines);
            JOptionPane.showMessageDialog(this, "PO verified successfully.");

            // ======== GENERATE PAYMENT ==========
            String paymentID = IDGenerator.generateNextID("P", "Payment.txt");
            String status = "UNPAID";
            Payment payment = new Payment(paymentID, status, poID);

            // Append to Payment.txt
            FileOperation.WriteFile(payment);

            JOptionPane.showMessageDialog(this, "Payment record generated with ID: " + paymentID);

            loadPOTable();
            clearDetails();
        } else {
            JOptionPane.showMessageDialog(this, "Failed to verify PO.");
        }
    }//GEN-LAST:event_btnVerifyActionPerformed

