    private void btnApproveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnApproveActionPerformed
        int row = tblRequisitions.getSelectedRow();
        if (row == -1) {
            JOptionPane.showMessageDialog(this, "Please select a requisition to approve.");
            return;
        }
        
        int confirm = JOptionPane.showConfirmDialog(this, "Approve this requisition?", "Confirm", JOptionPane.YES_NO_OPTION);
        if (confirm != JOptionPane.YES_OPTION) return;
        
        String prID = tblRequisitions.getValueAt(row, 0).toString(); // PR ID
        String itemID = tblRequisitions.getValueAt(row, 1).toString(); // Item ID
        String itemName = tblRequisitions.getValueAt(row, 2).toString(); // Item Name
        String quantity = tblRequisitions.getValueAt(row, 3).toString(); // Quantity (Restock Qty)

        // Pass data to GeneratePO form
        String newPOID = IDGenerator.generateNextID("PO", "PurchaseOrder.txt");
        GeneratePO generatePOPage = new GeneratePO(user, newPOID, prID, itemID, itemName, quantity, false);
        generatePOPage.setVisible(true);
        this.dispose();
    }//GEN-LAST:event_btnApproveActionPerformed

    private void btnRejectActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRejectActionPerformed
        int row = tblRequisitions.getSelectedRow();
        if (row == -1) {
            JOptionPane.showMessageDialog(this, "Please select a requisition to reject.");
            return;
        }

        String prID = tblRequisitions.getValueAt(row, 0).toString();

        int confirm = JOptionPane.showConfirmDialog(this, "Reject this requisition?", "Confirm", JOptionPane.YES_NO_OPTION);
        if (confirm != JOptionPane.YES_OPTION) return;

        POManager poManager = new POManager();
        if (poManager.rejectRequisition(prID)) {
            JOptionPane.showMessageDialog(this, "Requisition rejected and removed.");
            loadRequisitionTable();
            clearDetails();
        } else {
            JOptionPane.showMessageDialog(this, "Failed to reject requisition.");
        }
    }//GEN-LAST:event_btnRejectActionPerformed

