    private void tblRequisitionsMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblRequisitionsMouseClicked
        int row = tblRequisitions.getSelectedRow();
        if (row >= 0) {
            txtPRID.setText(tblRequisitions.getValueAt(row, 0).toString());
            txtItemID.setText(tblRequisitions.getValueAt(row, 1).toString());
            txtItemName.setText(tblRequisitions.getValueAt(row, 2).toString());
            txtRestockQty.setText(tblRequisitions.getValueAt(row, 3).toString());

            // Parse date string to Date object
            String dateStr = tblRequisitions.getValueAt(row, 5).toString();
            try {
                SimpleDateFormat format = new SimpleDateFormat("yyyy-MM-dd");
                Date date = format.parse(dateStr);
                txtRequiredDate.setDate(date);
            } catch (Exception e) {
                System.err.println("Failed to parse date: " + e.getMessage());
            }

            txtStatus.setText(tblRequisitions.getValueAt(row, 6).toString());
        }
    }//GEN-LAST:event_tblRequisitionsMouseClicked

