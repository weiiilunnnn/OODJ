    private void loadRequisitionTable() {
        DefaultTableModel model = new DefaultTableModel(new String[] {
            "PR ID", "Item ID", "Item Name", "Quantity", "Raised By", "Required Date", "Status"
        }, 0);

        List<String> lines = FileOperation.ReadFileAsList(PurchaseRequisition.class);
        Map<String, String> itemMap = FileOperation.getItemMap(); // Get item ID to name map

        for (String line : lines) {
            String[] parts = line.split(",");
            if (parts.length == 6 && parts[5].equalsIgnoreCase("Pending")) {
                String itemID = parts[1];
                String itemName = itemMap.getOrDefault(itemID, "Unknown Item");

                model.addRow(new Object[] {
                    parts[0],       // PR ID
                    itemID,         // Item ID
                    itemName,       // Item Name (dynamic)
                    parts[2],       // Quantity
                    parts[3],       // Raised By
                    parts[4],       // Required Date
                    parts[5]        // Status
                });
            }
        }

        tblRequisitions.setModel(model);
    }
    
