    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        if (!validateInputs()){
            return;
        }

        String SID = IDGenerator.generateNextID("SP", "Supplier.txt");
        Supplier s1 = new Supplier(SID, txtSupplierName.getText().trim(), txtContactNumber.getText().trim(), txtSupplierAddress.getText().trim());
        sm.add(s1, supplierList);
        tableModel.addRow(new Object[] {
            s1.getSupplierID(),
            s1.getSupplierName(),
            s1.getContactNo(),
            s1.getSupplierAddress()
        });
        JOptionPane.showMessageDialog(null, "Supplier added successfully.");
        clearFields();
    }//GEN-LAST:event_btnAddActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        int selectedRow = tblSupplier.getSelectedRow();
        if (selectedRow < 0) {
            JOptionPane.showMessageDialog(this, "Choose a supplier to delete.");
            return;
        }

        String itemId = tblSupplier.getValueAt(selectedRow, 0).toString();
        String itemName = tblSupplier.getValueAt(selectedRow, 1).toString();

        int confirm = JOptionPane.showConfirmDialog(this, "Are you sure you want to delete item \""
            + itemName + "\" (ID: " + itemId + ")?", "Confirm Deletion", JOptionPane.YES_NO_OPTION);

        if (confirm == JOptionPane.YES_OPTION) {
            sm.delete(itemId, supplierList);
            tblSupplier.setModel(sm.getSupplierTableModel());
            JOptionPane.showMessageDialog(this, "Supplier deleted successfully.");

            //btnAddItem.setEnabled(true);
            clearFields();
            tblSupplier.clearSelection();
        } else {
            JOptionPane.showMessageDialog(this, "Deletion cancelled.");
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

