    private void filterSuppliers(String keyword) {
        if (keyword == null || keyword.trim().isEmpty()) {
            sorter.setRowFilter(null); 
        } else {
            sorter.setRowFilter(RowFilter.regexFilter("(?i)" + keyword, 0, 1));
        }
    }

    private String capitalizeEachWord(String input) {
        String[] words = input.trim().toLowerCase().split("\\s+");
        StringBuilder capitalized = new StringBuilder();
        for (String word : words) {
            if (!word.isEmpty()) {
                capitalized.append(Character.toUpperCase(word.charAt(0)))
                           .append(word.substring(1)).append(" ");
            }
        }
        return capitalized.toString().trim();
    }
    
    private boolean validateInputs(){
        if (txtSupplierName.getText().trim().isEmpty() || txtContactNumber.getText().trim().isEmpty() || txtSupplierAddress.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Insufficient Credentials. Supplier Entry Declined.");
            return false;
        }
        
        if (txtContactNumber.getText().trim().matches(".*[a-zA-Z]+.*")){
            JOptionPane.showMessageDialog(null, "Invalid contact number. Letters are not allowed.");
            return false;
        }
        return true;
    }

